# ╔═╡ ebf7c539-ff05-4bf3-a9e9-09bd4b7f814c
function tree_rmse(Xtrain, ytrain, Xtest, ytest, Xips, yips)
    LGBMRegressor = MLJ.@load LGBMRegressor pkg = LightGBM

    base_model = LGBMRegressor()
    tuned_mach = machine(base_model, Xtrain, ytrain)
    fit!(tuned_mach, verbosity=1)

    # Predict on test set
    ŷ_test = predict(tuned_mach, Xtest)
    rmse_test = rms(ŷ_test, ytest)
    r2_test = rsquared(ŷ_test, ytest)

    println("Test RMSE = ", round(rmse_test, sigdigits=4))
    println("Test R²   = ", round(r2_test, sigdigits=4))

    # Predict on IPS set
    ŷ_ips = predict(tuned_mach, Xips)
    rmse_ips = rms(ŷ_ips, yips)
    r2_ips = rsquared(ŷ_ips, yips)

    return rmse_test, rmse_ips
end

# ╔═╡ e34886e3-734d-445a-b4f1-958845639ede
function dataset_performance(dataset, algorithm, Xt, y, Xt_ips, y_ips)
    performance = []
    for rep in 1:5, fold in 1:5
        result = load_method_split(SPLIT_ROOT, dataset, algorithm, rep, fold)
        y_train, y_test = DataSplits.splitdata(result, y)

        Xt_train = selectrows(Xt, result.train)
        Xt_test = selectrows(Xt, result.test)

        out = tree_rmse(Xt_train, y_train, Xt_test, y_test, Xt_ips, y_ips)
        push!(performance, out)
    end
    return performance
end

